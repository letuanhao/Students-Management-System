    private void loadListtbl(List<HocSinh> ds) {
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_hocsinh.getModel();
        tblmodel.setRowCount(0);
        if (ds == null) {

        } else {
            for (int i = 0; i < ds.size(); i++) {
                HocSinh hs = ds.get(i);
                tblmodel.addRow(new Object[]{hs.getMaHS(),hs.getSoHieu(),hs.getHotenHS()
                        ,hs.getGioiTinh(),hs.getDienThoai(),hs.getTrangThai()});
            }
        }
    }

