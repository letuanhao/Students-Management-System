    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String MaHS = txt_tmahs.getText().trim();
        String SoHieu = txt_tsohieu.getText().trim();
        String HotenHS = txt_thotenhs.getText().trim();
        String MaDanToc = DataStatic.dsDT.get(cb_tdantoc.getSelectedIndex()).getMaDanToc();
        String MaChinhSach = DataStatic.dsDSC.get(cb_tdsc.getSelectedIndex()).getMaChinhsach();
        String MaTonGiao = DataStatic.dsTG.get(cb_ttongiao.getSelectedIndex()).getMaTonGiao();
        String GioiTinh ="";
        if(rb_tnam.isSelected()){
            GioiTinh = DataStatic.GTnam;
        }else{
            GioiTinh = DataStatic.GTnu;
        }
        String DiaChi = txt_tdiachi.getText().trim();
        String DienThoai = txt_tsdt.getText().trim();
        String GhiChu = txt_tghichu.getText().trim();
        String TrangThai = (String) cb_ttrangthai.getSelectedItem();
        HocSinh hs = new HocSinh(MaHS, SoHieu, HotenHS, MaDanToc, MaChinhSach, MaTonGiao, GioiTinh, DiaChi, DienThoai, GhiChu, TrangThai);
        dao = new HocSinh_DAO();
        int kt = dao.Them(hs);
        if(kt ==1){
            
            dshs = dao.getALL();
            loadListtbl(dshs);
            dl_themhocsinh.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(fmHocSinh.this, "Thêm không thành công");
        }
    }//GEN-LAST:event_btn_themActionPerformed

