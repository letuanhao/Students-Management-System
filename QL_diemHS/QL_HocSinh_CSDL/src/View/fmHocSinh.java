    private void mn_xoahsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mn_xoahsActionPerformed
        // TODO add your handling code here:
        int index = tbl_hocsinh.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(fmHocSinh.this, "Bạn chưa chọn học sinh");
        } else {
            int ck = JOptionPane.showConfirmDialog(jScrollPane5, " Xóa học sinh : " + dshs.get(index).getMaHS(), "Xác nhận", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (ck == JOptionPane.YES_OPTION) {

                dao = new HocSinh_DAO();
                int kt = dao.Xoa(dshs.get(index));
                if (kt == 1) {
                    dshs.remove(index);
                    loadListtbl(dshs);
                } else {
                    JOptionPane.showMessageDialog(fmHocSinh.this, "Xóa không thành công");
                }
            }
        }
    }//GEN-LAST:event_mn_xoahsActionPerformed

