    private void openForm(int chedo) {
        if (chedo == THEM) {
            GiaoVien gv = new GiaoVien("", "", "", "", "", "", "", "");
            isedit = false;
            dl_addeditGV.setTitle("Thêm thông tin giáo viên");
            btn_addedit.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/add.png"))); // NOI18N
            btn_addedit.setText("Thêm");
            btn_addedit.setEnabled(true);
            btn_huy.setEnabled(false);
            ghidulieu(gv);
            txt_maGV.setText(automaGV());

        } else if (chedo == SUA) {
            isedit = true;
            dl_addeditGV.setTitle("Sửa thông tin giáo viên");
            btn_addedit.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/save.png"))); // NOI18N
            btn_addedit.setText("Lưu");
            btn_addedit.setEnabled(true);
            btn_huy.setEnabled(true);
            ghidulieu(editgv);

        } else if (chedo == XEMTT) {
            dl_addeditGV.setTitle("Thông tin giáo viên");
            btn_addedit.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icon/save.png"))); // NOI18N
            btn_addedit.setText("Lưu");
            btn_addedit.setEnabled(false);
            btn_huy.setEnabled(false);
            ghidulieu(editgv);
        }
        dl_addeditGV.setSize(700, 350);
        dl_addeditGV.setLocationRelativeTo(null);
        dl_addeditGV.setResizable(false);
        dl_addeditGV.setVisible(true);
    }

    private void ghidulieu(GiaoVien gv) {
        txt_maGV.setText(gv.getMaGV());
        txt_tenGV.setText(gv.getHoTenGV());
        txt_sdt.setText(gv.getDienThoai());
        txt_ghichu.setText(gv.getGhiChu());
        txt_diachi.setText(gv.getDiachi());
        cb_chuyenmon.setSelectedItem(gv.getChuyenMon());
        cb_trinhdo.setSelectedItem(gv.getTrinhDo());
        if (gv.getGioitinh().compareTo(DataStatic.GTnam) == 0) {
            rb_nam.setSelected(true);
        } else {
            rb_nu.setSelected(true);
        }
    }

    private String automaGV() {
        String magv = "20220";
        magv = magv + String.valueOf(dao.getmaGVmax() + 1);
        return magv;
    }

    private void loadListtbl(List<GiaoVien> ds) {
        DefaultTableModel tblmodel = (DefaultTableModel) tbl_dsGV.getModel();
        tblmodel.setRowCount(0);
        if (ds == null) {

        } else {
            for (GiaoVien gv : ds) {
                tblmodel.addRow(new Object[]{gv.getMaGV(), gv.getHoTenGV(), gv.getGioitinh(), gv.getDienThoai(), gv.getChuyenMon(), gv.getTrinhDo()});
            }
        }
    }

    private void loadcb() {
        cb_trinhdo.removeAllItems();
        for(String td:DataStatic.TD){
            cb_trinhdo.addItem(td);
        }

        cb_chuyenmon.removeAllItems();
        for(String cm:DataStatic.CM){
            cb_chuyenmon.addItem(cm);
        }

    }
