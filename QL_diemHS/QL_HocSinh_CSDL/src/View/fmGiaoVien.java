    private void btn_addeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addeditActionPerformed
        // TODO add your handling code here:\
        String maGV = txt_maGV.getText().trim();
        String HoTenGV = txt_tenGV.getText().trim();
        String gioitinh = "";
        if (rb_nam.isSelected()) {
            gioitinh = DataStatic.GTnam;
        } else {
            gioitinh = DataStatic.GTnu;
        }
        String Diachi = txt_diachi.getText().trim();
        String ChuyenMon = (String) cb_chuyenmon.getSelectedItem();
        String TrinhDo = (String) cb_trinhdo.getSelectedItem();
        String DienThoai = txt_sdt.getText().trim();
        String GhiChu = txt_ghichu.getText().trim();
        GiaoVien gv = new GiaoVien(maGV, HoTenGV, gioitinh, Diachi, ChuyenMon, TrinhDo, DienThoai, GhiChu);
        if (HoTenGV.length() == 0) {
            JOptionPane.showMessageDialog(fmGiaoVien.this, "Tên không dược để trống");
        } else {
            if (isedit) {
                int check = dao.Sua(gv);
                if (check == 0) {
                    dl_addeditGV.setVisible(false);
                    JOptionPane.showMessageDialog(fmGiaoVien.this, "Lưu không thành công");
                } else {
                    dl_addeditGV.setVisible(false);
                    dsgv = dao.getALL();
                    loadListtbl(dsgv);
                }
            } else {
                int check = dao.Them(gv);
                if (check == 0) {
                    dl_addeditGV.setVisible(false);
                    JOptionPane.showMessageDialog(fmGiaoVien.this, "Thêm không thành công");
                } else {
                    dl_addeditGV.setVisible(false);
                    dsgv = dao.getALL();
                    loadListtbl(dsgv);
                }
            }
        }

    }//GEN-LAST:event_btn_addeditActionPerformed

    private void mn_suaGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mn_suaGVActionPerformed
        // TODO add your handling code here:
        int index = tbl_dsGV.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(fmGiaoVien.this, "Bạn chưa chọn giáo viên");
        } else {
            editgv = dsgv.get(index);
            openForm(SUA);
        }

    }//GEN-LAST:event_mn_suaGVActionPerformed

    private void mn_xoaGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mn_xoaGVActionPerformed
        // TODO add your handling code here:
        int index = tbl_dsGV.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(fmGiaoVien.this, "Bạn chưa chọn giáo viên");
        } else {
            int ck = JOptionPane.showConfirmDialog(fmGiaoVien.this, " Xóa TT GV : " + dsgv.get(index).getMaGV(), "Xác nhận", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (ck == JOptionPane.YES_OPTION) {
                int check = dao.Xoa(dsgv.get(index));
                if (check == 0) {
                    JOptionPane.showMessageDialog(fmGiaoVien.this, "Xóa không thành công");
                } else {
                    dsgv = dao.getALL();
                    loadListtbl(dsgv);
                }
            }

        }
    }//GEN-LAST:event_mn_xoaGVActionPerformed

